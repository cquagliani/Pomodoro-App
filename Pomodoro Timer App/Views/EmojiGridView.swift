struct EmojiGridView: View {
    @Binding var colorMode: AppColorMode
    let title: String
    @Binding var emojiSelection: String
    @Binding var isPresented: Bool
    @State private var searchText = ""

    private let columns: [GridItem] = Array(repeating: .init(.flexible()), count: 6)

    private var filteredCategories: [EmojiCategory] {
        if searchText.isEmpty {
            return allEmojiCategories
        }
        let lowered = searchText.lowercased()
        return allEmojiCategories.compactMap { category in
            // If category name matches, return the whole category
            if category.name.lowercased().contains(lowered) {
                return category
            }
            // Filter emojis by keyword match
            let matched = category.emojis.filter { emoji in
                if let keywords = emojiKeywords[emoji] {
                    return keywords.contains { $0.contains(lowered) }
                }
                return false
            }
            if matched.isEmpty { return nil }
            return EmojiCategory(name: category.name, emojis: matched)
        }
    }

    var body: some View {
        ZStack {
            Color.theme.primaryColor.edgesIgnoringSafeArea(.all)

            NavigationView {
                ScrollView {
                    LazyVStack(alignment: .leading, spacing: 16) {
                        ForEach(filteredCategories) { category in
                            Section {
                                LazyVGrid(columns: columns, spacing: 8) {
                                    ForEach(category.emojis, id: \.self) { emoji in
                                        Button(action: {
                                            emojiSelection = emoji
                                            isPresented = false
                                        }) {
                                            Text(emoji)
                                                .font(.title2)
                                                .frame(width: 44, height: 44)
                                                .background(emojiSelection == emoji ? Color.gray.opacity(0.5) : Color.clear)
                                                .cornerRadius(8)
                                        }
                                    }
                                }
                            } header: {
                                Text(category.name)
                                    .font(.subheadline)
                                    .fontWeight(.semibold)
                                    .foregroundColor(.secondary)
                                    .padding(.horizontal, 4)
                            }
                        }
                    }
                    .padding()
                }
                .scrollDismissesKeyboard(.immediately)
                .searchable(text: $searchText, prompt: "Search emojis")
                .navigationBarTitle(Text(title), displayMode: .inline)
                .toolbar {
                    ToolbarItem(placement: .navigationBarTrailing) {
                        Button("Done") {
                            isPresented = false
                        }
                        .foregroundColor(.blue)
                        .padding()
                    }
                }
            }
        }
        .modifier(ColorModeViewModifier(mode: colorMode))
    }
}
